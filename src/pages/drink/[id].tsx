import { type NextPage } from "next";
import Head from "next/head";
import { useRouter } from "next/router";
import { api } from "../../utils/api";

const EditDrinkPage: NextPage = () => {
  const router = useRouter();
  const { id } = router.query as {
    id: string;
  };
  const singleDrink = api.drinks.getDrinkById.useQuery({ id });

  return (
    <>
      <Head>
        <title>EditDrinkPage</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen">
        <section className="container mx-auto py-10">
          <h1 className="text-4xl">{singleDrink.data?.title}</h1>
          <pre>
            <code>{singleDrink.data?.price}</code>
          </pre>
        </section>
      </main>
    </>
  );
};

export default EditDrinkPage;
