import { type NextPage } from "next";
import Head from "next/head";
import { useState } from "react";
import { api } from "../utils/api";
import { useRouter } from "next/router";

const SubmitDrink: NextPage = () => {
  const [productTitle, setProductTitle] = useState("");
  const [productPrice, setProductPrice] = useState("");
  const createDrinkMutation = api.drinks.createDrink.useMutation();
  const router = useRouter();

  const handleSubmitDrink = async (e: React.FormEvent) => {
    e.preventDefault();
    await createDrinkMutation.mutateAsync({
      title: productTitle,
      price: productPrice,
    });
    void router.push("/drinks");
  };

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gray-800">
        <div className="w-1/3">
          <form className="flex flex-col gap-5" onSubmit={handleSubmitDrink}>
            <div className="flex flex-col gap-1">
              <div className="form-control w-full max-w-xs">
                <label className="label">
                  <span className="label-text">Title</span>
                </label>
                <input
                  type="text"
                  placeholder="Type here"
                  className="input-bordered input w-full max-w-xs"
                  value={productTitle}
                  onChange={(e) => setProductTitle(e.target.value)}
                  required
                />
              </div>
              <div className="form-control w-full max-w-xs">
                <label className="label">
                  <span className="label-text">Price</span>
                </label>
                <input
                  type="text"
                  placeholder="Type here"
                  className="input-bordered input w-full max-w-xs"
                  value={productPrice}
                  onChange={(e) => setProductPrice(e.target.value)}
                  required
                />
              </div>
            </div>
            <button className="btn-primary btn max-w-xs">Submit</button>
          </form>
        </div>
      </main>
    </>
  );
};

export default SubmitDrink;
